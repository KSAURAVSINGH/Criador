const AccountSchema = {
    email: {
        type: Email,
        required: true
    },
    firstName: {
        type: String,
        required: true
    },
    lastName: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    salt: {
        type: String,
        required: true
    },
    verified: {
        type: Boolean,
        default: false
    }
}